   # Operators in tkinter 
    # 1. Button ---> use to display the buttons. 
    # 2. Canvas ---> Canvas is used to draw polygons,ovals,lines e.t.c in your application.
    # 3. Checkbutton ---> It is used for display number of options as checkboxes. The user can select multiple options at a time.
    # 4. Entry ---> The Entry widget is used to display single-line-text-field for taking input from the user.
    # 5. Frame ---> The Frame widget is used as container widget to store anothe widgets.
    # 6. Label ---> The Label widget is used to provide single line captions to another widgets. It can also contain images.
    # 7. Listbox ---> The Listbox widget is used to provide a list of options to user.
    # 8. Menubutton ---> The Menubutton is used to display menus in the application.
    # 9. Menu ---> The Menu widget is used to provide various commands to a user. These commands are containe inside Menubuttons.
    # 10. Message ---> The Message widget is used to display multiline text fields for accespting values from user.
    # 11. Radiobutton ---> The Radiobutton is used to display number of options in the form of Radiobuttons to user. The user can select only one option a time.
    # 12. Scale ---> The Scale widget is used to provide a slider widget.
    # 13. Scrollbar ---> The scrollbar widget is used to add scrolling capability to various widgets such as list boxes. 
    # 14. Text ---> The Text widget is used to display multiple-lines.
    # 15. Toplevel ---> The Toplevel is used to provide seprate window container.
    # 16. Spinbox ---> The Spinbox widget is a variant of the standard Tkinter Entry widget, which can be used to select from a fixed number of values.
    # 17. PanedWindow ---> A PanedWindow is a container widget that may contain any number of panes, arranged horizontally or vertically.
    # 18. LabelFrame ---> A labelframe is a simple container widget. Its primary purpose is to act as a spacer or container for complex window layouts.
    # 19. tkMessageBox ---> This module is used to display message boxes in your applications.


